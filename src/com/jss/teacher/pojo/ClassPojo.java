package com.jss.teacher.pojo;

public class ClassPojo {
	private String Cno;
	private String Cname;
	private int Cnum;
	private String Academy;
	private String Major;
	private String Ctime_start;
	private String Ctime_end;
	public String getCno() {
		return Cno;
	}
	public void setCno(String cno) {
		Cno = cno;
	}
	public String getCname() {
		return Cname;
	}
	public void setCname(String cname) {
		Cname = cname;
	}
	public int getCnum() {
		return Cnum;
	}
	public void setCnum(int cnum) {
		Cnum = cnum;
	}
	public String getAcademy() {
		return Academy;
	}
	public void setAcademy(String academy) {
		Academy = academy;
	}
	public String getMajor() {
		return Major;
	}
	public void setMajor(String major) {
		Major = major;
	}
	public String getCtime_start() {
		return Ctime_start;
	}
	public void setCtime_start(String ctime_start) {
		Ctime_start = ctime_start;
	}
	public String getCtime_end() {
		return Ctime_end;
	}
	public void setCtime_end(String ctime_end) {
		Ctime_end = ctime_end;
	}
}
